def safe_folder_name(value):
    cleaned = re.sub(r'[<>:"/\\|?*]+', "-", str(value).strip())
    cleaned = re.sub(r"\s+", " ", cleaned).strip(" .")
    return cleaned or "category"


def category_id(index):
    return f"champ-{index:06d}"


def make_item(index, name, parent, parent_id, depth, sort_order, path):
    return {
        "id": category_id(index),
        "name": name,
        "url": "",
        "parent": parent,
        "parentId": parent_id,
        "oneCId": str(index),
        "sortOrder": sort_order,
        "active": True,
        "showHome": depth == 1,
        "mainImageName": "",
        "iconName": "",
        "source": "the-champ",
        "champId": index,
        "depth": depth,
        "path": " / ".join(path),
        "createdAt": datetime.now().isoformat(timespec="seconds"),
        "priorityGroup": depth,
    }


def flatten_tree():
    items = []
    index = 1
    for root_order, (root, children) in enumerate(sorted(CHAMP_TREE.items(), key=lambda item: item[0].lower()), start=1):
        root_index = index
        root_id = category_id(root_index)
        items.append(make_item(root_index, root, "Без категории", "", 1, root_order, [root]))
        index += 1
        for sub_order, (subcategory, types) in enumerate(sorted(children.items(), key=lambda item: item[0].lower()), start=1):
            sub_index = index
            sub_id = category_id(sub_index)
            items.append(make_item(sub_index, subcategory, root, root_id, 2, root_order * 1000 + sub_order, [root, subcategory]))
            index += 1
            for type_order, product_type in enumerate(sorted(types, key=lambda item: item.lower()), start=1):
                items.append(make_item(
                    index,
                    product_type,
                    subcategory,
                    sub_id,
                    3,
                    root_order * 100000 + sub_order * 100 + type_order,
                    [root, subcategory, product_type],
                ))
                index += 1
    return items


def clear_category_dirs(categories_dir):
    for item in categories_dir.iterdir():
        if item.is_dir():
            shutil.rmtree(item)


def write_tree_folders(categories_dir, categories):
    by_parent = {}
    for item in categories:
        by_parent.setdefault(item["parentId"], []).append(item)
    for group in by_parent.values():
        group.sort(key=lambda item: item["name"].lower())

    def walk(parent_id, parent_folder):
        for item in by_parent.get(parent_id, []):
            folder = parent_folder / safe_folder_name(item["name"])
            suffix = 2
            while folder.exists() and (folder / "category.json").exists():
                folder = parent_folder / safe_folder_name(f"{item['name']} {suffix}")
                suffix += 1
            folder.mkdir(parents=True, exist_ok=True)
            next_item = {**item, "folderPath": str(folder)}
            (folder / "category.json").write_text(json.dumps(next_item, ensure_ascii=False, indent=2), encoding="utf-8")
            walk(item["id"], folder)

    walk("", categories_dir)


def rebuild(root):
    root = Path(root)
    categories_dir = root / "katalog" / "Категории"
    categories_file = categories_dir / "categories.json"
    backup_dir = root / "katalog" / "backups"
    backup_dir.mkdir(parents=True, exist_ok=True)
    categories_dir.mkdir(parents=True, exist_ok=True)

    if categories_file.exists():
        stamp = datetime.now().strftime("%Y%m%d-%H%M%S")
        shutil.copy2(categories_file, backup_dir / f"categories-before-champ-{stamp}.json")

    categories = flatten_tree()
    clear_category_dirs(categories_dir)
    categories_file.write_text(json.dumps(categories, ensure_ascii=False, indent=2), encoding="utf-8")
    write_tree_folders(categories_dir, categories)
    return categories_file, len(categories)


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--root", action="append", required=True)
    args = parser.parse_args()

    for root in args.root:
        categories_file, total = rebuild(root)
        print(f"{categories_file}: champ_categories={total}")


if __name__ == "__main__":
    main()
